package com.example.hellowordsem9.models;

public class Libro {
    public int id;
    public String titulo;
    public String resumen;
    public String autor;
    public String img;
    public double latitude;
    public double longitude;

    public Libro() {
        this.id = id;
        this.titulo = titulo;
        this.resumen = resumen;
        this.autor = autor;
        this.img = img;
        this.latitude = latitude;
        this.longitude = longitude;
    }


}